    public ArmorManagementServer() {
        initComponents();
        
        String serviceName = "rmi://127.0.0.1:12340/ArmorManagement";
        String fileName = "armor.txt";
        ArmorServer server = null;
        this.setResizable(false);
        
        try {
            LocateRegistry.createRegistry(12340);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Server could not be started repeatedly");
            System.exit(0);
        }
        
        try {
            server = new ArmorServer(fileName);
            Naming.rebind(serviceName, server);
            lbNotification.setText("Service " + serviceName + " is running!");
            pbStarting.setIndeterminate(true);
        } catch (RemoteException ex) {
            Logger.getLogger(ArmorManagementServer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(ArmorManagementServer.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }

